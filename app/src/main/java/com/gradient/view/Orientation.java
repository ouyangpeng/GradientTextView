package com.gradient.view;

/**
 * TextView 颜色变化的方向
 */
public enum Orientation {
    /**
     * 从左到右
     */
    LEFT_TO_RIGHT,
    /**
     * 从左到右，从无到右
     */
    LEFT_TO_RIGHT_FORM_NONE,
    /**
     * 从右到左
     */
    RIGHT_TO_LEFT,
    /**
     * 从右到左，从无到右
     */
    RIGHT_TO_LEFT_FROM_NONE,
    /**
     * 从中间到两边
     */
    INNER_TO_OUTER
}
